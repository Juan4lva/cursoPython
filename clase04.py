# Hallar el promedio de tres notas
# DATOS:

'''
N1 = Nota uno
N2 = Nota dos
N3 = Nota tres

P = PROMEDIO
'''

N1 = int(input('ingresar primera nota: '))
N2 = int(input('ingresar segunda nota: '))
N3 = int(input('ingresar tercera nota: '))

P = (N1 + N2 + N3)/3

print('el promedio de las notas es: '+str(P)+' PUNTOS.')